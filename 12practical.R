#==================================================================
# VERTICAL COMBINATION (rbind) WITH TABLE DISPLAY
#==================================================================

# Load required libraries
library(readr)
library(DT)  # For interactive table (optional)

# Load original dataset with UTF-8 encoding
Bengali_hate_speech_ <- read_csv(
  "C:/Users/IT-28/Downloads/Bengali hate speech .csv",
  locale = locale(encoding = "UTF-8")
)

# Create new rows to add
new_data <- data.frame(
  sentence = c(
    "?????????????????? ???????????? ??????????????? ?????????????????????!!!!!",
    "???????????? ???????????? ?????? ???????????????????????? ??????????????? ???????????????",
    "????????????????????? ??????????????? ????????????????????? ???????????? ????????? ?????? ???????????? ?????????"
  ),
  hate = c(1, 1, 1),
  category = c("sports", "sports", "politics"),
  stringsAsFactors = FALSE
)

# Vertical combination: add new rows
combined_data <- rbind(Bengali_hate_speech_, new_data)

# Summary of the combination
cat("--- Combined Data Summary ---\n")
cat("Original rows:", nrow(Bengali_hate_speech_), "\n")
cat("New rows added:", nrow(new_data), "\n")
cat("Total rows (Expected):", nrow(Bengali_hate_speech_) + nrow(new_data), "\n")
cat("Total rows (Actual):", nrow(combined_data), "\n\n")

# Display table in console (wide view)
options(tibble.width = Inf)
print(head(combined_data, 6))
print(tail(combined_data, 6))

# Open spreadsheet-like table in RStudio Viewer
View(combined_data)

# Interactive table (scrollable) in RStudio Viewer / browser
datatable(combined_data)

# Save the updated dataset with UTF-8 encoding
write_csv(
  combined_data,
  "C:/Users/IT-28/Downloads/Bengali_hate_speech_updated.csv"
)
